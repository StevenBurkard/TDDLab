const addNumbers = (numOne, numTwo) => {
    return numOne + numTwo;
}

const subtractNumbers = (numOne, numTwo) => {
    return numOne - numTwo;
}

const multiplyNumbers = (numOne, numTwo) => {
    return numOne * numTwo;
}

const divideNumbers = (numOne, numTwo) => {
    return numOne / numTwo;
}

export { addNumbers, subtractNumbers, multiplyNumbers, divideNumbers };